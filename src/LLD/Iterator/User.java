package LLD.Iterator;

public class User {
    int userId;
    String userName;

    public User(String userName, int id) {
        userId = id;
        this.userName = userName;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }
}
